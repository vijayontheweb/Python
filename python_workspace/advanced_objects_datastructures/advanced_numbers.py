# 0x100 -> Converts into hexadecimal representation of numbers
print(hex(256))
# 0b100000000 -> Converts into binary representation of numbers
print(bin(256))
# 16 -> Same as 2**4
print(pow(2, 4))
# 3 - absolute value
print(abs(-3))
# 4 - rounds the number to a given precision in decimal
print(round(3.6))
# 3.14
print(round(3.141592, 2))
